Skybox new_Skybox() {
	Skybox this = (Skybox)malloc(sizeof(struct Skybox));
	if (!this) return NULL;

    Shader shader = new_Shader("shaders\\skybox.vert", NULL, "shaders\\skybox.frag");
    Shader_uniformLocations[UNIFORM_SKYBOX_MATRIX] = glGetUniformLocation(shader->id, "rotProjMatrix");
    this->renderer = new_RenderComponent(shader, 0, 0, 0, 1);

	glGenTextures(1, &this->renderer->texture);
	glBindTexture(GL_TEXTURE_CUBE_MAP, this->renderer->texture);

	int width, height, nrChannels;
	unsigned char* data;
    for (unsigned int i = 0; i < 6; i++) {
        unsigned char* data = stbi_load(textures[i], &width, &height, &nrChannels, 0);
        if (data) {
            glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
            stbi_image_free(data);
        }
        else {
            printf("Could not load cubemap\n");
            stbi_image_free(data);
        }
    }
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);

    unsigned int skyboxVAO, skyboxVBO;
    glGenVertexArrays(1, &skyboxVAO);
    glGenBuffers(1, &skyboxVBO);
    glBindVertexArray(skyboxVAO);
    glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), &vertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    this->renderer->VAO = skyboxVAO;
    this->renderer->VBO = skyboxVBO;

    return this;
}

void Skybox_free(Skybox skybox) {
    Shader_free(skybox->renderer->shader);
    RenderComponent_free(skybox->renderer);
    free(skybox);
}